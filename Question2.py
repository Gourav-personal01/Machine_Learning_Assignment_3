# Q2. How does the Wrapper method differ from the Filter method in feature selection?

# Answer -
 
# In wrapper method -  It uses a specific machine learning algorithm (e.g., decision tree, support vector machine) 
# to evaluate the performance of different subsets of features. It selects the subset that yields the best performance according 
# to a chosen evaluation metric 

# In filter Method - Filter method in feature selection is used to calculate the dataset with some statistical criteria and 
# after that if the critera met then we will use that predictive output dataset. 


