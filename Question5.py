# Q5. In which situations would you prefer using the Filter method over the Wrapper method for feature
# selection?

# Answer - 

# High-Dimensional Data:
# Filter methods are often more computationally efficient than Wrapper methods.

# Quick Initial Screening:
# Filter methods are useful for quickly conducting an initial feature screening to identify obviously irrelevant features
    
# No Access to Target Label:
# In some cases, you may not have access to the target label during the feature selection process.